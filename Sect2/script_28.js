// Use of The conditional (Ternary operator), because it has 3 parts
// the condition, the if part and the else part

const age = 19;

const drink = age >= 18 ? 'wine' : 'water';
console.log(drink);

// Variation
console.log(`I like to drink ${age >= 18 ? 'wine' : 'water'}`);

